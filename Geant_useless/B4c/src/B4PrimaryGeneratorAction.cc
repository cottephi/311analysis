B4PrimaryGeneratorAction::B4PrimaryGeneratorAction()
 : G4VUserPrimaryGeneratorAction(),
   fParticleGun(nullptr)
{
  G4int nofParticles = 1;
  fParticleGun = new G4ParticleGun(nofParticles);
  vector <MyPrimary> primaries;
  read_simu_tree(primaries);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

B4PrimaryGeneratorAction::~B4PrimaryGeneratorAction()
{
  delete fParticleGun;
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B4PrimaryGeneratorAction::GeneratePrimaries(G4Event* anEvent)
{
  // This function is called at the begining of event

  // In order to avoid dependence of PrimaryGeneratorAction
  // on DetectorConstruction class we get world volume 
  // from G4LogicalVolumeStore
  //
  
  G4double worldZHalfLength = 0.;
  auto worldLV = G4LogicalVolumeStore::GetInstance()->GetVolume("world");
  
  int evtid = anEvent->GetEventID();
  MyPrimary primary = primaries.back();
  
  // Set gun
  auto particleDefinition 
    = G4ParticleTable::GetParticleTable()->FindParticle("mu-");
  fParticleGun->SetParticleDefinition(particleDefinition);
  fParticleGun->SetParticleMomentumDirection(G4ThreeVector(primary.start_py, primary.start_pz, primary.start_px));
  fParticleGun->SetParticleEnergy(primary.start_E);
  fParticleGun
    ->SetParticlePosition(G4ThreeVector(primary.start_y, primary.start_z, primary.start_x));

  fParticleGun->GeneratePrimaryVertex(anEvent);
  primaries.pop_back();
  if(primaries.size() == 0){
    G4RunManager::GetRunManager()->AbortRun(true);
  }
}

